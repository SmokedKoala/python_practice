

def f22(num):
    a = (num & ((0x1 << 10) - 0x1))
    b = (num & ((0x1 << 26) - (0x1 << 10))) >> 10
    c = (num & ((0x1 << 29) - (0x1 << 25))) >> 26
    d = (num & (0x1 << 29)) >> 29
    e = (num & (0x1 << 30)) >> 30
    f = (num & (0x1 << 31)) >> 31
    return hex((d << 31) | (f << 30) | (b << 14) | (c << 11) | (a << 1) | e)

